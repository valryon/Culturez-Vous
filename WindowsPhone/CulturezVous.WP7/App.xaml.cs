        /// <summary>
        /// Provides easy access to the root frame of the Phone Application.
        /// </summary>
        /// <returns>The root frame of the Phone Application.</returns>
        public PhoneApplicationFrame RootFrame { get; private set; }

        public bool IsRunning { get; private set; }

        public static bool IsLowMemoryDevice
        {
            get
            {
                object result;
                Int64 iResult = 0;

                if (DeviceExtendedProperties.TryGetValue("ApplicationWorkingSetLimit", out result))
                {
                    iResult = (Int64)result;
                }
                else
                {
                    iResult = DeviceStatus.ApplicationMemoryUsageLimit;
                }

                return (iResult < 94371840L);
            }
        }

